let value = 4.25

console.log("Original Value", value)

//rounded()
console.log("rounded: ", Math.round(value))

//ceil() 
console.log("ceiling value: ", Math.ceil(value))

//floor()
console.log("floor value: ", Math.floor(value))

//random() to get a random value
console.log("Random value: ", Math.random())

//max() 
console.log("Max in (5, 20, 4): ", Math.max(5, 20, 4))

//min()
console.log("Min in (5, 20, 4): ", Math.min(5, 20, 4))

//pow() 
console.log("Power of 4: ", Math.pow(4, 2))

//sqrt()
console.log("Square root of 4: ", Math.sqrt(4))
